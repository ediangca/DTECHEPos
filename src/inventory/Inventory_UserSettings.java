    public void showQuery(String query) {
        resettable();
        try {
            statement = connection.createStatement();
            System.err.println(query);
            result = statement.executeQuery(query);
            while (result.next()) {
                //user_ID, Fullname, Department, Position, isActive, Datetime_Created, Datetime_Updated
                user_dtm.addRow(new Object[]{result.getString(1),
                    result.getString(2),
                    result.getString(3),
                    result.getString(4),
                    result.getBoolean(5),
                    result.getString(6),
                    result.getString(7)});
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

