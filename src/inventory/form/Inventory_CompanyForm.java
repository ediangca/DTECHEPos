    private void but_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_saveActionPerformed
        // TODO add your handling code here:
        if (validate_field()) {
            JOptionPane.showConfirmDialog(this, validated_fields, "D-TECH WARNING", JOptionPane.PLAIN_MESSAGE, JOptionPane.WARNING_MESSAGE);
            return;
        }
        boolean active = true;
        if (combo_compisactive.getSelectedIndex() > 1) {
            active = false;
        }
        try {

            if (radioisdefault.isSelected()) {
                statement = connection.createStatement();
                statement.executeUpdate("update `company` set isdefault = 0;");
                statement.close();
            }
            if (but_save.getText().equalsIgnoreCase("save")) {

                statement = connection.createStatement();
//                Company_No., Company_Name, Owner, Contact_No., Email_Address, Address, Logo, isActive, Datetime_Created, Datetime_Updated, isDefault
                statement.executeUpdate("insert into company values('"
                        + field_compno.getText() + "','"
                        + field_compname.getText() + "','"
                        + field_compowner.getText() + "','"
                        + field_comptelno.getText() + "','"
                        + field_compemail.getText() + "','"
                        + field_compaddress.getText().trim() + "','"
                        + field_compaddress1.getText().trim() + "',null,"
                        + active + ",'"
                        + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','"
                        + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "',0," + radioisdefault.isSelected() + ")");

                JOptionPane.showConfirmDialog(this, "Company Successfully Added!", "D-TECH INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);

            } else {
                statement = connection.createStatement();
                statement.executeUpdate("update company set Company_Name ='"
                        + field_compname.getText() + "', Owner ='"
                        + field_compowner.getText() + "', `Contact_No.` ='"
                        + field_comptelno.getText() + "', Email_Address ='"
                        + field_compemail.getText() + "', Address1 ='"
                        + field_compaddress.getText().trim() + "', Address2 ='"
                        + field_compaddress.getText().trim() + "', isActive ="
                        + active + ",Datetime_Updated ='"
                        + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "', isdefault ="
                        + radioisdefault.isSelected() + " where `Company_No.` = '"
                        + Company_No + "'");

                JOptionPane.showConfirmDialog(this, "Company Successfully Updated!", "D-TECH INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }

            statement.close();
            inventory_systemconfig.refreshtable();
            clearfield();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, e.getMessage(), "D-TECH WARNING", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_but_saveActionPerformed


