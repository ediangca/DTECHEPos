    private void restoredata(String company_no) {
        try {
            statement = connection.createStatement();
//            Company_No., Company_Name, Owner, Contact_No., Email_Address, Address1, Address2, Logo, isActive, Datetime_Created, Datetime_Updated, isDefault
            result = statement.executeQuery("select * from company where `Company_No.` = '" + company_no + "'");
            if (result.next()) {
                field_compno.setText(result.getString(1));
                field_compname.setText(result.getString(2));
                field_compowner.setText(result.getString(3));
                field_comptelno.setText(result.getString(4));
                field_compemail.setText(result.getString(5));
                field_compaddress.setText(result.getString(6));
                field_compaddress1.setText(result.getString(7));
                if (result.getBoolean(9)) {
                    combo_compisactive.setSelectedIndex(1);
                } else {
                    combo_compisactive.setSelectedIndex(2);
                }
                if (result.getBoolean(12)) {
                    radioisdefault.setSelected(true);
                } else {
                    radioisdefault.setSelected(false);
                }
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

