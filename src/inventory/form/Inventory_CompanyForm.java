    private boolean validate_field() {
        boolean validate = false;
        validated_fields = "Please check the ff. fields:\n\n";
        if (field_compno.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company No..\n";
            validate = true;
            return validate;
        }
        if (field_compname.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company Name.\n";
            validate = true;
            return validate;
        }
        if (field_compowner.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company Owner.\n";
            validate = true;
            return validate;
        }
        if (field_comptelno.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company Tel No..\n";
            validate = true;
            return validate;
        }
        if (field_compemail.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company Email Address.\n";
            validate = true;
            return validate;
        }
        if (field_compaddress.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company Address 1.\n";
            validate = true;
            return validate;
        }
        if (field_compaddress.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Company Address 2.\n";
            validate = true;
            return validate;
        }
        return validate;
    }

