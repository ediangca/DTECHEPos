    public void showQuery(String query) {
        resettable();

        try {
            statement = connection.createStatement();
            result = statement.executeQuery(query);

//              "Discount No.", "Name", "Description", "Amount", "isActive", "Created", "Updated"
//                    Discount_No., Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive
            while (result.next()) {
                discount_dtm.addRow(new Object[]{
                    result.getString(1),
                    result.getString(2),
                    result.getString(3),
                    new DecimalFormat("#0.00").format(result.getDouble(4)),
                    result.getBoolean(8),
                    result.getString(5),
                    result.getString(6)});
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void restoredata(String discountno) {
        try {
            statement = connection.createStatement();
//            Discount_No., Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive
            System.out.println("SELECT `Discount_No.`, Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive FROM discount d where "
                    + "`Discount_No.` = '" + discountno + "'");
            result = statement.executeQuery("SELECT `Discount_No.`, Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive FROM discount d where "
                    + "`Discount_No.` = '" + discountno + "'");
            if (result.next()) {
                Discount_No = result.getString(1);
                field_name.setText(result.getString(2));
                field_description.setText(result.getString(3));
                field_amount.setText(new DecimalFormat("#0.00").format(result.getDouble(4)));
                field_name.setEnabled(true);
                field_description.setEnabled(true);
                field_amount.setEnabled(true);
                if (result.getBoolean(8)) {
                    but_inactive.setText("INACTIVE");
                } else {
                    but_inactive.setText("ACTIVE");
                }

            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

