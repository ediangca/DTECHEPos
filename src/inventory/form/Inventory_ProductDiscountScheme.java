    private void but_inactiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_inactiveActionPerformed
        // TODO add your handling code here:
        try {
            boolean active;
            if (but_inactive.getText().equalsIgnoreCase("INACTIVE")) {
                active = false;
            } else {
                active = true;
            }
            statement = connection.createStatement();
            System.out.println("update discount set isActive = " + active + ", "
                    + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "' where `Discount_No.` = '" + Discount_No + "';");
            statement.executeUpdate("update discount set isActive = " + active + ", "
                    + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "' where `Discount_No.` = '" + Discount_No + "';");

            field_name.setText(null);
            field_description.setText(null);
            field_amount.setText("0.00");

            statement.close();
            JOptionPane.showConfirmDialog(this, "Unit Successfully " + but_inactive.getText() + "TED", "INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);

            showQuery("SELECT * FROM discount d;");

            Discount_No = "";
            field_name.setText(null);
            field_description.setText(null);
            field_name.setEnabled(false);
            field_description.setEnabled(false);
            field_amount.setEnabled(false);
            but_newsaveupdate.setText("New");
            but_newsaveupdate.setVisible(true);
            but_inactive.setVisible(false);
            but_cancel.setVisible(false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_but_inactiveActionPerformed

