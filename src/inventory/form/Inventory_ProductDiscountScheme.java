    private void but_newsaveupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_newsaveupdateActionPerformed
        // TODO add your handling code here:

        try {
            if (but_newsaveupdate.getText().equalsIgnoreCase("NEW")) {
                System.out.println("inventory.form.Inventory_ProductDiscountScheme.but_newsaveupdateActionPerformed()");
                field_name.setText("");
                field_description.setText("");
                field_amount.setText("");
                field_name.setEnabled(true);
                field_description.setEnabled(true);
                field_amount.setEnabled(true);
                but_cancel.setVisible(true);
                but_newsaveupdate.setText("SAVE");
            } else if (but_newsaveupdate.getText().equalsIgnoreCase("save")) {
                if (field_name.getText().isEmpty() || field_description.getText().isEmpty()) {
                    JOptionPane.showConfirmDialog(this, "Either your Input is empty or Invalid\n\nPlease check you input(s)", "D-TECH WARNING", JOptionPane.PLAIN_MESSAGE, JOptionPane.WARNING_MESSAGE);
                    return;
                }
                statement = connection.createStatement();
                System.out.println("select * from discount where `name` = '" + field_name.getText() + "'");
                result = statement.executeQuery("select * from discount where `name` = '" + field_name.getText() + "'");
                if (result.next()) {
                    JOptionPane.showConfirmDialog(this, "Discount already exist!", "WARNING", JOptionPane.PLAIN_MESSAGE, JOptionPane.WARNING_MESSAGE);
                    return;
                } else {
                    statement.close();
                    statement = connection.createStatement();
//                    Discount_No., Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive
                    System.out.println("insert into discount (Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive)values('"
                            + field_name.getText() + "','"
                            + field_description.getText() + "',"
                            + field_amount.getText() + ",'"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','" + inventory_productlist.inventory_mainframe.userID + "',0)");
                    statement.executeUpdate("insert into discount (Name, Description, Amount, Datetime_Created, Datetime_Updated, User_ID, isActive)values('"
                            + field_name.getText() + "','"
                            + field_description.getText() + "',"
                            + field_amount.getText() + ",'"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','" + inventory_productlist.inventory_mainframe.userID + "',0)");

                    JOptionPane.showConfirmDialog(this, "Discount scheme Successfully Added!", "INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
                    but_cancelActionPerformed(evt);
                }

            } else if (but_newsaveupdate.getText().equalsIgnoreCase("update")) {
                boolean active;
                if (but_inactive.getText().equalsIgnoreCase("INACTIVE")) {
                    active = false;
                } else {
                    active = true;
                }
                statement = connection.createStatement();
                System.out.println("update discount set  name = '" + field_name.getText() + "', description = '" + field_description.getText() + "', amount = '" + field_amount.getText() + "', "
                        + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "', User_ID = '" + inventory_productlist.inventory_mainframe.userID + "' where `Discount_No.` = '" + Discount_No + "';");
                statement.executeUpdate("update discount set  name = '" + field_name.getText() + "', description = '" + field_description.getText() + "', amount = '" + field_amount.getText() + "', "
                        + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "', User_ID = '" + inventory_productlist.inventory_mainframe.userID + "' where `Discount_No.` = '" + Discount_No + "';");

                field_name.setText(null);
                field_description.setText(null);
                field_amount.setText("0.00");

                statement.close();

                JOptionPane.showConfirmDialog(this, "Discount scheme Successfully Updated!", "INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
                but_cancelActionPerformed(evt);
            }

            showQuery("SELECT * FROM discount d;");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_but_newsaveupdateActionPerformed

