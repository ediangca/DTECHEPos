    private void but_inactiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_inactiveActionPerformed
        // TODO add your handling code here:
        try {
            boolean active;
            if (but_inactive.getText().equalsIgnoreCase("INACTIVE")) {
                active = false;
            } else {
                active = true;
            }
            statement = connection.createStatement();
            System.out.println("update user_position set isActive = " + active + ", "
                    + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "' where `Position_No.` = '" + Position_No + "';");
            statement.executeUpdate("update user_position set isActive = " + active + ", "
                    + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "' where `Position_No.` = '" + Position_No + "';");

            field_position.setText(null);

            statement.close();
            JOptionPane.showConfirmDialog(this, "Position Successfully " + but_inactive.getText() + "TED", "INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);

            showQuery("select concat(`Position_No.`,'-', `Position`)as Position, isActive, Datetime_Created, Datetime_Updated from user_position p;");

            Position_No = "";
            field_position.setText(null);
            field_position.setEnabled(false);
            but_newsaveupdate.setText("New");
            but_newsaveupdate.setVisible(true);
            but_inactive.setVisible(false);
            but_cancel.setVisible(false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_but_inactiveActionPerformed

