    private void resettable() {
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Position", "isActive", "Created", "Updated"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.Object.class, java.lang.Boolean.class, java.lang.Object.class, java.lang.Object.class
            };
            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });

        if (jTable1.getColumnModel().getColumnCount() > 0) {
            jTable1.getColumnModel().getColumn(0).setMinWidth(250);
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(250);
            jTable1.getColumnModel().getColumn(0).setMaxWidth(250);
            jTable1.getColumnModel().getColumn(1).setMinWidth(60);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(60);
            jTable1.getColumnModel().getColumn(1).setMaxWidth(60);
        }

        position_dtm = (DefaultTableModel) jTable1.getModel();
    }

    public void showQuery(String query) {
        resettable();

        try {
            statement = connection.createStatement();
            result = statement.executeQuery(query);
            while (result.next()) {
//              `Position_No.`, `Position`, isActive, DatetimeCreated, DatetimeUpdated
                position_dtm.addRow(new Object[]{result.getString(1),
                    result.getBoolean(2),
                    result.getString(3),
                    result.getString(4)});
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void restoredata(String position) {
        try {
            statement = connection.createStatement();
            System.out.println("select `Position_No.`, `Position`, isActive, Datetime_Created, Datetime_Updated from user_position where "
                    + "concat(`Position_No.`,'-', `Position`) = '" + position + "'");
            result = statement.executeQuery("select `Position_No.`, `Position`, isActive, Datetime_Created, Datetime_Updated from user_position where "
                    + "concat(`Position_No.`,'-', `Position`) = '" + position + "'");
            if (result.next()) {
                Position_No = result.getString(1);
                field_position.setText(result.getString(2));
                field_position.setEnabled(true);
                if (result.getBoolean(3)) {
                    but_inactive.setText("INACTIVE");
                } else {
                    but_inactive.setText("ACTIVE");
                }

            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

