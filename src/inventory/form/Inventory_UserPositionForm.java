    private void but_newsaveupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_newsaveupdateActionPerformed
        // TODO add your handling code here:
        if (but_newsaveupdate.getText().equalsIgnoreCase("new")) {
            but_newsaveupdate.setText("SAVE");
            field_position.setText(null);
            field_position.setEnabled(true);
            but_cancel.setVisible(true);
        } else if (but_newsaveupdate.getText().equalsIgnoreCase("save")) {
            if (field_position.getText().isEmpty()) {
                JOptionPane.showConfirmDialog(this, "Either your Input is empty or Invalid\n\nPlease check you input(s)", "D-TECH WARNING", JOptionPane.PLAIN_MESSAGE, JOptionPane.WARNING_MESSAGE);
                return;
            }
            try {
                statement = connection.createStatement();
                System.out.println("select * from user_position where `position` = '" + field_position.getText() + "'");
                result = statement.executeQuery("select * from user_position where `position` = '" + field_position.getText() + "'");
                if (result.next()) {
                    JOptionPane.showConfirmDialog(this, "Position already exist!", "WARNING", JOptionPane.PLAIN_MESSAGE, JOptionPane.WARNING_MESSAGE);
                    return;
                } else {
                    System.out.println("insert into user_position(`Position`, isActive, Datetime_Created, Datetime_Updated, User_ID)values"
                            + "('" + field_position.getText()
                            + "',true,'"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','" + inventory_usersettings.inventory_mainframe.userID + "')");
                    statement.executeUpdate("insert into user_position(`Position`, isActive, Datetime_Created, Datetime_Updated, User_ID)values"
                            + "('" + field_position.getText()
                            + "',true,'"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','"
                            + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "','" + inventory_usersettings.inventory_mainframe.userID + "')");

                    JOptionPane.showConfirmDialog(this, "Position Successfully Added!", "INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
                    field_position.setText(null);

                    showQuery("select concat(`Position_No.`,'-', `Position`)as Position, isActive, Datetime_Created, Datetime_Updated from user_position p;");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        } else if (but_newsaveupdate.getText().equalsIgnoreCase("update")) {
            try {
                boolean active;
                if (but_inactive.getText().equalsIgnoreCase("INACTIVE")) {
                    active = false;
                } else {
                    active = true;
                }
                statement = connection.createStatement();
                System.out.println("update user_position set position = '" + field_position.getText() + "', "
                        + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "', User_ID = '" + inventory_usersettings.inventory_mainframe.userID + "' where `Position_No.` = '" + Position_No + "';");
                statement.executeUpdate("update user_position set position = '" + field_position.getText() + "', "
                        + "Datetime_Updated = '" + new SimpleDateFormat("yyyy-MM-d HH:mm:ss").format(new Date()) + "', User_ID = '" + inventory_usersettings.inventory_mainframe.userID + "' where `Position_No.` = '" + Position_No + "';");
                field_position.setText(null);

                statement.close();

                JOptionPane.showConfirmDialog(this, "Position Successfully Updated!", "INFORMATION", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE);

                showQuery("select concat(`Position_No.`,'-', `Position`)as Position, isActive, Datetime_Created, Datetime_Updated from user_position p;");

                Position_No = "";
                field_position.setText(null);
                field_position.setEnabled(false);
                but_newsaveupdate.setText("New");
                but_newsaveupdate.setVisible(true);
                but_inactive.setVisible(false);
                but_cancel.setVisible(false);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_but_newsaveupdateActionPerformed

