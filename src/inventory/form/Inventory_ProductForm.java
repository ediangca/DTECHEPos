    private String getgeneratedproductNo() {
        try {
            statement = connection.createStatement();
            System.out.println("select concat('101',year(now()),month(now()),day(now()),'000',substr(`Product_No.`,15)+1) as nextProductNo, `Product_No.` from product where date(Datetime_Created) = '" + new SimpleDateFormat("yyyy-MM-d").format(new Date()) + "' order by (substr(`Product_No.`,15)+1) desc limit 1");
            result = statement.executeQuery("select concat('101',year(now()),month(now()),day(now()),'000',substr(`Product_No.`,15)+1) as nextProductNo, `Product_No.` from product where date(Datetime_Created) = '" + new SimpleDateFormat("yyyy-MM-d").format(new Date()) + "' order by (substr(`Product_No.`,15)+1) desc limit 1");
            if (result.next()) {
                Product_No = result.getString(1);

            } else {
                Product_No = inventory_productlist.inventory_mainframe.Company_No
                        + new SimpleDateFormat("yyyyMMdd").format(new Date()) + "0001";
            }
            statement.close();
            result.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
        return Product_No;
    }

    private void restoredata() {
        try {
            populateproductunit();
            populateproductcategory();
//            Product_No., Barcode, Short_Description, Description, Type, Category, Quantity, Buyby, isActive, isVatable, Company_No., Datetime_Created, Datetime_Updated, cancelflag, User_ID, Smallunit, Bigunit, Noperunit
            statement = connection.createStatement();
            result = statement.executeQuery("SELECT * FROM product p where `Product_No.` = '" + Product_No + "'");
            if (result.next()) {
                field_psdescription.setText(result.getString(3));
                field_pdescription.setText(result.getString(4));

                combo_ptype.setSelectedItem(result.getString(5));
                combo_pcategory.setSelectedItem(result.getString(6));
                label_pstock.setText(result.getDouble(7) + "");

                switch (result.getInt(8)) {
                    case 1:
                        combo_pbuybytype.setSelectedIndex(0);
                        break;
                    case 6:
                        combo_pbuybytype.setSelectedIndex(1);
                        break;
                    case 12:
                        combo_pbuybytype.setSelectedIndex(2);
                        break;
                    default:
                        combo_pbuybytype.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"PIECE", "HALF DOZEN", "DOZEN", (result.getInt(8) + "")}));
                        combo_pbuybytype.setEditable(true);
                        combo_pbuybytype.setSelectedIndex(3);
                }
                if (result.getBoolean(9)) {
                    combo_pisactive.setSelectedIndex(1);
                } else {
                    combo_pisactive.setSelectedIndex(2);
                }

                radio_pisvatable.setSelected(result.getBoolean(10));
                radio_pisbookable.setSelected(result.getBoolean(11));

                combo_psunit.setSelectedItem(result.getString(17));
                combo_pbunit.setSelectedItem(result.getString(18));
                combo_pnounit.setValue(result.getDouble(19));
                
                ResultSet result1 = statement.executeQuery("SELECT New_Cost FROM product_cost p where `Product_No.` = '" + Product_No + "'");
                if(result1.next()){
                    field_pcost.setText(new DecimalFormat("##0.00").format(result1.getDouble(1)));
                }
                result1.close();
                ResultSet result2 = statement.executeQuery("SELECT New_Price FROM product_price p where `Product_No.` = '" + Product_No + "'");
                if(result2.next()){
                    field_pprice.setText(new DecimalFormat("##0.00").format(result2.getDouble(1)));
                }
                result2.close();
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void populateproductunit() {
        combo_psunit.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Select"}));
        combo_pbunit.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Select"}));
        try {
            statement = connection.createStatement();
            result = statement.executeQuery("SELECT * FROM product_unit p where unittype = 'Small Unit' and isActive = 1;");
            while (result.next()) {
                combo_psunit.addItem(result.getString(3));
            }
            statement.close();
            result.close();
            statement = connection.createStatement();
            result = statement.executeQuery("SELECT * FROM product_unit p where unittype = 'Big Unit' and isActive = 1;");
            while (result.next()) {
                combo_pbunit.addItem(result.getString(3));
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

