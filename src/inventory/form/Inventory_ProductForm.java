    private boolean validate_field() {
        boolean validate = false;
        if (field_productno.getText().isEmpty()) {
            validated_fields = validated_fields + "* Please Enter Product No.\n";
            validate = true;
        }
//        if (field_barcode.getText().isEmpty()) {
//            validated_fields = validated_fields + "* Invalid or Existing Barcode No.\n";
//            validate = true;
//        }
        if (field_psdescription.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Short description\n";
            validate = true;
        }
        if (field_pdescription.getText().isEmpty()) {
            validated_fields = validated_fields + "* Invalid Input for Description\n";
            validate = true;
        }
        if (combo_ptype.getSelectedIndex() == 0) {
            validated_fields = validated_fields + "* Please select Product Type\n";
            validate = true;
        }
        if (combo_pcategory.getSelectedIndex() == 0) {
            validated_fields = validated_fields + "* Please select Product Category\n";
            validate = true;
        }
        if (combo_pisactive.getSelectedIndex() == 0) {
            validated_fields = validated_fields + "* Select Status option.\n";
            validate = true;
            return validate;
        }
//        if (combo_pbuybytype.getSelectedIndex() == 0) {
//            validated_fields = validated_fields + "* Select Buy by type option.\n";
//            validate = true;
//            return validate;
//        }
//        if (combo_pisbookable.getSelectedIndex() == 0) {
//            validated_fields = validated_fields + "* Select Bookable option.\n";
//            validate = true;
//            return validate;
//        }
        try {
            statement = connection.createStatement();
            result = statement.executeQuery("select * from product where short_description = '" + field_psdescription.getText() + "' or description = '" + field_pdescription.getText() + "' group by `product_no.` having  `product_no.` not in('" + Product_No + "')");
            if (result.next()) {
                validated_fields = validated_fields + "* Either Short Description and Description already exist.\n";
                return validate;
            }
            statement.close();
            result.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        return validate;
    }

